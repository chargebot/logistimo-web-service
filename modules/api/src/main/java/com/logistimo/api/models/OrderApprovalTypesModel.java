public class OrderApprovalTypesModel {
  /**
   * approval type
   */
  private String type;
  /**
   * approval id
   */
  private String id;
  /**
   * total number of approvals for that order
   * @return
   */
  private Integer count;
  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public Integer getCount() {
    return count;
  }

  public void setCount(Integer count) {
    this.count = count;
  }

}
